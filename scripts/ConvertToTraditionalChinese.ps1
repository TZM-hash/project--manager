$extensions = @(".cshtml", ".cs")
$excludedDirs = @("obj", "bin", "Migrations")

$files = Get-ChildItem -Path $rootPath -Recurse -File | Where-Object {
    $extensions -contains $_.Extension -and
    -not ($excludedDirs | Where-Object { $_.FullName -match "[\\/]$($_)[\\/]" -or $_.FullName -match "[\\/]$($_)$" })
}

$changedFiles = 0
$totalReplacements = 0

foreach ($file in $files) {
    $content = Get-Content -Path $file.FullName -Raw -Encoding UTF8
    $original = $content
    $fileReplacements = 0
    
    foreach ($rep in $replacements) {
        if ($content.Contains($rep[0])) {
            $content = $content.Replace($rep[0], $rep[1])
            $fileReplacements++
        }
    }
    
    if ($content -ne $original) {
        Set-Content -Path $file.FullName -Value $content -Encoding UTF8
        $changedFiles++
        $totalReplacements += $fileReplacements
        Write-Host "已轉換: $($file.FullName.Replace($rootPath, ''))"
    }
}

